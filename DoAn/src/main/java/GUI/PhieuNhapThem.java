    private void BTThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTThemActionPerformed
      // TODO add your handling code here:
      try {
        int selected = tableSanPham.getSelectedRow();
        if (selected == -1) {
          JOptionPane.showMessageDialog(rootPane, "Vui lòng chọn 1 sản phẩm", "Lỗi", JOptionPane.ERROR_MESSAGE);
          return;
        }
        String masp = tableSanPham.getValueAt(selected, 0).toString().trim();
        String tensp = tableSanPham.getValueAt(selected, 1).toString().trim();
        int gianhap = Integer.parseInt(gianhapTxt.getText());
        int soluong = Integer.parseInt(slNhapTxt.getText().trim());
        addRowCTPhieuNhap(masp, tensp, soluong, gianhap);
        tinhTongtien();
      } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(rootPane, "Thông tin không hộp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
      }
    }//GEN-LAST:event_BTThemActionPerformed

    private void BTLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTLuuActionPerformed
      // TODO add your handling code here:
      if (tableCTPhieuNhap.getRowCount() == 0) {
        JOptionPane.showMessageDialog(rootPane, "Phiếu nhập cần ít nhất 1 sản phẩm", "Chưa có sản phẩm", JOptionPane.ERROR_MESSAGE);
        return;
      }
      String nhaCc;
      int tongTien = this.tongTien;
      if (nhaCcTxt.getText().isEmpty()) {
        nhaCc = "Unknown";
      } else {
        nhaCc = nhaCcTxt.getText().trim();
      }
      String username = Login.username;
      int maNh = PhieuNhapHangBLL.insertPhieuNhapHang(nhaCc, username, tongTien);
      for (int i = 0; i < tableCTPhieuNhap.getRowCount(); i++) {
        int maSp = Integer.parseInt(tableCTPhieuNhap.getValueAt(i, 1).toString());
        int slNhap = (int) tableCTPhieuNhap.getValueAt(i, 3);
        int giaNhap = (int) tableCTPhieuNhap.getValueAt(i, 4);
        CTPhieuNhapBLL.insertCTPhieuNhap(maSp, maNh, slNhap, giaNhap);
      }
      QuanLyPhieuNhap.loadAllPhieuNhapHang();
      resetLayout();
      JOptionPane.showMessageDialog(rootPane, "Thêm phiếu nhập thành công");
    }//GEN-LAST:event_BTLuuActionPerformed
  public void resetLayout() {
    DefaultTableModel CTHDmodel = (DefaultTableModel) tableCTPhieuNhap.getModel();
    while (tableCTPhieuNhap.getRowCount() > 0) {
      CTHDmodel.removeRow(0);
    }
    slNhapTxt.setText("");
    gianhapTxt.setText("");
  }
