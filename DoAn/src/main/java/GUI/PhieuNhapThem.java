  public void tinhTongtien() {
    int tongtien = 0;
    Locale localeEN = new Locale("en", "EN");
    NumberFormat en = NumberFormat.getInstance(localeEN);
    for (int i = 0; i < tableCTPhieuNhap.getRowCount(); i++) {
      tongtien += (int) tableCTPhieuNhap.getValueAt(i, 4);
    }
    this.tongTien = tongtien;
    tongtienTxt.setText(en.format(tongtien));
    return;
  }

  public int getRowbyId(String id) {
    for (int i = 0; i < tableCTPhieuNhap.getRowCount(); i++) {
      if (Integer.parseInt(id) == Integer.parseInt(tableCTPhieuNhap.getValueAt(i, 1).toString())) {
        return i;
      }
    }
    return -1;
  }

  public void addRowCTPhieuNhap(String masp, String tensp, int soluong, int gianhap) {
    if (getRowbyId(masp) == -1) {
      this.stt++;
      int trigia = gianhap * soluong;
      Object[] row = {this.stt, masp, tensp, soluong, trigia};
      DefaultTableModel model = (DefaultTableModel) tableCTPhieuNhap.getModel();
      model.addRow(row);
    } else {
      int existRow = getRowbyId(masp);
      int triGiaHienTai = (int) tableCTPhieuNhap.getValueAt(existRow, 4);
      int slHientai = (int) tableCTPhieuNhap.getValueAt(existRow, 3);
      tableCTPhieuNhap.setValueAt(slHientai + soluong, existRow, 3);
      tableCTPhieuNhap.setValueAt(triGiaHienTai + (soluong * gianhap), existRow, 4);
    }
  }

